// 畫迷宮

function drawMaze(ctx){

    for(let y=0;y<MAP_HEIGHT;y++){

        for(let x=0;x<MAP_WIDTH;x++){

            if(maze[y][x]===1){

                ctx.fillStyle="#303030";

            }else{

                ctx.fillStyle="#111111";

            }

            ctx.fillRect(

                x*TILE_SIZE,

                y*TILE_SIZE,

                TILE_SIZE,

                TILE_SIZE

            );

            ctx.strokeStyle="#1d1d1d";

            ctx.strokeRect(

                x*TILE_SIZE,

                y*TILE_SIZE,

                TILE_SIZE,

                TILE_SIZE

            );

        }

    }

}

// 是否可通行

function isWall(x,y){

    if(
        x<0||
        y<0||
        x>=MAP_WIDTH||
        y>=MAP_HEIGHT
    ){
        return true;
    }

    return maze[y][x]===1;

}
